int main (){
    unsigned char a=7, b = 8;
    /*operator &*/
    printf("7&8 = %d\n", a & b);

    /*operator |*/
    printf("a|b = %d\n", a | b);

    /*operator ^*/ 
    printf("a^b = %d\n", a ^ b);
 
    /*operator ~*/
    printf("~a = %d\n", a = ~a);
 
    /*operator <<*/
    printf("b<<1 = %d\n", b << 1);
 
    /*operator >>*/
    printf("b>>1 = %d\n", b >> 1);

}